# 두 점간 위치 간 최단거리 BFS로 구현
# S:[sy, sx], E = [ey, ex]
def bfs(S, E):
	sy, sx = S[0], S[1]
	ey, ex = E[0], E[1]
	dist[sy][sx] = 0
	Q = deque()
	Q.append([sy, sx])
	while Q:
		y, x = Q.popleft()
		for dy, dx in dir:
			ny, nx = y+dy, x+dx
			if 0<=ny<R and 0<=nx<C and map_city[ny][nx] != '*' and dist[ny][nx] > dist[y][x]+1:
				dist[ny][nx] = dist[y][x]+1
				Q.append([ny,nx])
	return dist[ey][ex]
